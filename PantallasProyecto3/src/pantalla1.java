    private void BrncomprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BrncomprarActionPerformed
        // TODO add your handling code here:
        txttotal.setText("");
        txtsubtotal.setText("");
        panel.setText("");
        
        Producto producto = new Producto();
        Cliente cliente = new Cliente();
        Factura factura = new Factura();
        String  nombre = "";
        
        String  descripcion = "";
        int codigo;
        
        
        if(txtcantidad.getText().equals("") && txtcodigo.getText().equals("") && txtdescripcion.getText().equals("") && txtprecio.getText().equals("")   ){
            JOptionPane.showMessageDialog(null, "Caja Vacia");
          return ;
        }
        
        codigo = Integer.parseInt( txtcodigo.getText());
        producto.setId(codigo);
        cantidad = Integer.parseInt( txtcantidad.getText() );
        producto.setCantidad(cantidad);
        descripcion = txtdescripcion.getText();
        producto.setDescripcion(descripcion);
        precio = Integer.parseInt(txtprecio.getText());
        
        producto.setPrecio(precio);
        
        cliente.setNombre( txtnombre.getText());
        cliente.setId( txtid.getText() );
        cliente.setDireccion(txtdireccion.getText());
        cliente.setTelefono(txtcelular.getText());
        
        factura.setFecha(txtfecha.getText());
        factura.setVendedor(txtvendedor.getText());
        
        
        Aproducto.add(producto);
        
        panel.append(" Cliente \n");
        panel.append(cliente.toString() + " \n" + " \n");
        panel.append(" Compras \n");
        for (int i = 0; i < Aproducto.size(); i++) {
           panel.append( Aproducto.get(i).toString() + " \n" ); 
        }
        
      
        
        
        
        
        
    }//GEN-LAST:event_BrncomprarActionPerformed

