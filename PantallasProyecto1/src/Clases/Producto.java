package Clases;

/**
 *
 * @author DELL
 */
public class Producto {
    private int id;
    private int precio;
    private int cantidad;

    public Producto() {
    }

    public Producto(int id, int precio, int cantidad) {
        this.id = id;
        this.precio = precio;
        this.cantidad = cantidad;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    @Override
    public String toString() {
        return "Producto{" + "id=" + id + ", precio=" + precio + ", cantidad=" + cantidad + '}';
    }
    
    

    
}
